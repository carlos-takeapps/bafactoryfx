        //0xffffffff = invalid 
        //Bit 0 = Reduced resolution 
        //Bit 1 = Single page 
        //Bit 2 = Transparency mask 
        //Bit 3 = TIFF/IT final page 
        //Bit 4 = TIFF-FX mixed raster content
